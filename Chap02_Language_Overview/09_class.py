#!/usr/bin/env python3
# A Class Example

class Car:
    def model(self):
        print('Car Model: Tesla')

    def color(self):
        print('Car Color: Black')

    def year(self):
        print('Make Year: 2020')

def main():
    cars = Car()
    cars.model()
    cars.color()
    cars.year()

if __name__ == '__main__': main()
